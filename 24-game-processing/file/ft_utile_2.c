#include "../cub3d.h"

int		ft_is_player(char c)
{
	if (c == 'W' || c == 'S' || c == 'E' || c == 'N')
		return (1);
	return (0);
}

int		ft_is_whitespace(char c)
{
	if ((c >= 9 && c <= 13) || c == 32)
		return (1);
	return (0);
}

int		ft_is_map(char c)
{
	if (c == '0' || c == '1' || c == '2')
		return (1);
	return (0);
}
